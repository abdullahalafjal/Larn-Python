# Example of addition
result = 5 + 3
print(result)

# Example of assignment with addition
x = 10
x += 5
print(x)

# Example of equality comparison
is_equal = 10 == 10
print(is_equal)

# Example of logical AND
a = True
b = False
result = a and b
print(result)

# Example of identity operator (is)
a = [1, 2, 3]
b = a
print(a is b)

# Example of membership operator (in)
my_list = [1, 2, 3, 4, 5]
print(3 in my_list)

# Example of bitwise AND
a = 10  # binary: 1010
b = 4  # binary: 0100
result = a & b
print(result)
