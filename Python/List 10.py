# Creating a list
fruits = ["apple", "banana", "cherry"]

# Accessing list elements
print(fruits[0])

# Modifying an element
fruits[1] = "blueberry"
print(fruits)

# Adding a new element
fruits.append("orange")
print(fruits)

# Removing an element
fruits.remove("apple")
print(fruits)

# List length
print(len(fruits))
